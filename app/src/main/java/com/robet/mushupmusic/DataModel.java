package com.robet.mushupmusic;

public class DataModel {
    String judul, ketererangan, videoid;

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getKetererangan() {
        return ketererangan;
    }

    public void setKetererangan(String ketererangan) {
        this.ketererangan = ketererangan;
    }

    public String getVideoid() {
        return videoid;
    }

    public void setVideoid(String videoid) {
        this.videoid = videoid;
    }
}
